##Tipos de variáveis:
# Python possui tipagem dinamica, o que significa que não é necessárío informar o tipo na hora de declarar uma função

##✓ Inteiros: valores positivos ou negativos, que não possuem uma parte fracionária.
##Exemplos: 1, 30, 40, 12, -50
inteiro: int = 1
##✓ Float (real): valores positivos ou negativos, que podem possuir uma parte
##fracionária (também podem ser inteiros). Exemplos: 1.4, 6.7, 10.3, 100, -47
pontoFlutante:float = 2.0
##✓ Caracteres (char ou string): qualquer elemento presente no teclado. Exemplos:
##“Maria”, “João”, ‘M’, ‘F’
cadeiaCaracteres:str = "Tipagem dinamica"
caractere = 'c'

##✓ Lógico (boleano): 
## verdadeiro ou falso. Exemplos: true, false, 1, 0

logico:bool = True

print(type(inteiro))
print(type(pontoFlutante))
print(type(cadeiaCaracteres))
print(type(caractere))

